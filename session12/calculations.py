import math as m

print('ln(2.5) =', m.log(2.5))
print('log10(6) =', m.log10(6))
print('[7.8] =', m.floor(7.8))
print('ceil(3.6) =', m.ceil(3.6))
print('e^4.2 =', m.sin(m.exp(4.2)))
print('sin(1deg) =', m.sin(m.degrees(1)))
print('sin(1rad) =', m.sin(m.radians(1)))
print('GCD of 8 and 6 =', m.gcd(8, 6))
print('LCM of 8 and 6 =', m.lcm(8, 6))
